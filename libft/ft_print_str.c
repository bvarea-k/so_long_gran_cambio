#include "libft.h"

int	ft_print_str(char *s)
{
	int	i;

	i = 0;
	if (s == 0)
		return (write(1, "(null)", 6));
	while (s && s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
